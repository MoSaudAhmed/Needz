package com.need.needz.model;

public class TouristSpot {
    public String name;
    public String city;
    public String hobbies;
    public int url;

    public TouristSpot(String name, String city, String hobbies, int url) {
        this.name = name;
        this.city = city;
        this.hobbies = hobbies;
        this.url = url;
    }
}
